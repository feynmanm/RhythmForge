package com.feynmanm.rhythmforge.instruments;

public abstract class XMLConverter {

	protected String instrumentElementTitle;
	protected String instrumentElementNameTitle;
	protected String instrumentDataTitle;
	protected String instrumentColorTitle;
	protected String soundElementTitle;
	protected String soundElementNameTitle;
	protected String data1Title;
	protected String data2Title;
	protected String data3Title;
	protected String data4Title;
	
	public String getInstrumentElementTitle() {
		return instrumentElementTitle;
	}

	public String getInstrumentElementNameTitle() {
		return instrumentElementNameTitle;
	}
	
	public String getInstrumentDataTitle() {
		return instrumentDataTitle;
	}
	
	public String getInstrumentColorTitle() {
		return instrumentColorTitle;
	}

	public String getSoundElementTitle() {
		return soundElementTitle;
	}

	public String getSoundElementNameTitle() {
		return soundElementNameTitle;
	}

	public String getData1Title() {
		return data1Title;
	}

	public String getData2Title() {
		return data2Title;
	}

	public String getData3Title() {
		return data3Title;
	}

	public String getData4Title() {
		return data4Title;
	}

}